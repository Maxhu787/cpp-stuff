# 11-3-7 印出字元圖形-7
'''
432101234
*********4
**** ****3
***   ***2
**     **1
*       *0
**     **1
***   ***2
**** ****3
*********4
'''

for i in range(-4,5):
    for j in range(-4,5):
        if abs(i)+abs(j) < 4:
            print(" ",end='')
        else:
            print("*",end='')
    print()
    
